void Robottino::ruota (int stepDelay) {
  currentServoMillis = millis();
  
  if (currentServoMillis - previousServoMillis >= stepDelay) {
    
    if (angle < - ANGLE_BOUND || angle > ANGLE_BOUND) {
      dir = -dir;
    }
    angle += dir * ANGLE_STEP;
    
    servo.write (angle + 90);
    
    previousServoMillis = currentServoMillis;
  }
}



int pitch;
unsigned long currentTime;
unsigned long previousTime = 0;
const byte thereminDelay = 10;

void buzzInit () {
  pinMode(BUZZ_PIN, OUTPUT);
}

void Robottino::theremin(int sensorValue) {  //without any calibration
  currentTime = millis();
  if (currentTime - previousTime >= thereminDelay) {
    if(sensorValue > 100) {
      pitch = map(sensorValue, 0, 1024, 40, 4000);  //if the sensor are the LDR, the value can be as large as 1024
    }
    else {
      pitch = map(sensorValue, 0, 50, 40, 4000);  //needed to have a good range of frequencies in the vicinity of the eyes
    }
    tone(BUZZ_PIN, pitch, 20);
    previousTime = currentTime;
  }
}

void Robottino::beep () {
  tone(BUZZ_PIN, NOTE_A7, 20);
}

